# include "lemin.h"

void	lem_trash_line2(char **s, int *i)
{
	char *tmp;

	*i = 0;
	while ((*s)[*i] && ((*s)[*i] == ' ' || (*s)[*i] == '\t'))
		++(*i);
	if (*i != 0)
	{
		tmp = *s;
		*s = strdup(*s + *i);
		free(tmp);
		*i = 0;
	}
}
