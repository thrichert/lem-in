# include "lemin.h"

int		id_next_str(char *s, int i)
{
	if (s[i] == '\0')
	{
		++i;
		while (s[i] == '\0')
			++i;
		return (i);
	}
	++i;
	while (s[i] != '\0')
		++i;
	++i;
	while (s[i] == '\0')
			++i;
	return (i);
}
