#include "p_str.h"

char		*ft_get_line(char *s, int *i)
{
	int		j;
	char	*s2;

	j = *i;
	while (s[j])
	{
		if (s[j] == '\n')
			break ;
		++j;
	}
	s2 = ft_strndup(s + *i, j - *i);
	*i = j + 1;
	return (s2);
}
