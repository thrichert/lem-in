#include "p_str.h"

void	ft_up_case(char *str)
{
	int i;

	if (!str)
		return ;
	i = 0;
	while (str[i])
	{
		if (str[i] > 96 && str[i] < 123)
			str[i] -= 32;
		++i;
	}
}
