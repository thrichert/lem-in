#include "p_str.h"

void	ft_tabscpy(char **d, char **s, int c, int l)
{
	int i;
	int	j;

	c = (c == 0) ? 2147483647 : c;
	l = (l == 0) ? 2147483647 : l;
	j = 0;
	while (j < l && s[j])
	{
		i = 0;
		while (i < c && s[j][i])
		{
			d[j][i] = s[j][i];
			i++;
		}
		j++;
	}
}
