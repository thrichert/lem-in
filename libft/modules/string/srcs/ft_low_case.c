#include "p_str.h"

void	ft_low_case(char *str)
{
	int		i;

	if (str)
	{
		i = 0;
		while (str[i])
		{
			if (str[i] > 64 && str[i] < 91)
				str[i] += 32;
			++i;
		}
	}
}
