#include "p_lst.h"

void	ft_lstiter(t_lst *lst, void (*f)(t_lst *elem))
{
	t_lst	*tmp;

	if (*f)
	{
		while (lst)
		{
			tmp = lst->next;
			(*f)(lst);
			lst = tmp;
		}
	}
}
